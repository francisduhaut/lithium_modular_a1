/* Crc basic routines */
uint CrcFwd(uint c, uchar s)
{
	/* Calculate new forward crc : crc(n) + new char = crc(n + 1) */
	/* c : previous crc */
	/* s : char added */
	/* return : new crc */
	return *(((uchar *)&c) + 1) ^ CrcTabFwd[*(uchar *)(&c) ^ s];
}

uint CrcRev(uint c, uchar s)
{
	/* Calculate new reverse crc : crc(n + 1) + new char = crc(n) */
	/* c : previous crc */
	/* s : char added */
	/* return : new crc */
	uchar n;
	n = CrcTabRev[HighByte(c)];
	return ((c ^ CrcTabFwd[n]) << 8) + (n ^ s);
}

uchar CrcChar(uint c, uint d, uchar *s)
{
	/* Calculate char from crc(n) and crc(n + 1) */
	/* c : previous crc */
	/* d : next crc */
	/* *s : calculated char */
	/* return : O = OK, 1 = error (impossible to find char with c and d) */
	*s = LowByte(c) ^ CrcTabRev[HighByte(d)];
	if (CrcFwd(c, *s) == d)
		return 0;
	else
		return 1;
}

void CrcAdd(uchar *s, uint n, uint m, uint l)
{
	/* Add crc at end of string (pf PF) */
	/* *s : address of string to be crc ed */
	/* n : pointer on 1st char */
	/* m : length of string (0 = 1 car) */
	/* l : length of buffer - 1 */
	uint  c;
	c = CrcCalc(&s[0], n, m, l);
	s[(n + m + 1) & l] = LowByte(c);
	s[(n + m + 2) & l] = HighByte(c);
}

uint CrcCalc(uchar *s, uint n, uint m, uint l)
{
	/* Calculate crc  */
	/* *s : address of string to be crc ed */
	/* n : pointer on 1st char */
	/* m : length of string (0 = 1 car) */
	/* l : length of buffer - 1 */
	uint  i;
	uint  j;
	uint  c;
	c = JBCRC;
	i = n;
	j = n + m;
	do
	{
		c = CrcFwd(c, s[i & l]);
		i ++;
	}
	while (i <= j);
	return c;
}

uchar CrcChk(uchar *s, uint n, uint m, uint l)
{
	/* Control crc at end of string (pf PF) */
	/* *s : address of string to be crc ed */
	/* n : pointeur sur la position de départ */
	/* m : longueur de la chaîne à sommer (0 = 1 car) */
	/* l : longueur du buffer - 1 */
	/* Return : 0 = no error, 1 = 1 error corrected */
	uint i;                       /* Pointer of first char */
	uchar t;                      /* Calculated char for correction */
	uint c;                       /* Calculated crc */
	uint d;                       /* Transmitted crc in string */
	uchar e;                      /* Eror counter */
	c = CrcCalc(&s[0], n, m, l);  /* Calculates normal crc */
	if (c != 0)
	{
		/* Checksum is false, try to correct */
		e = 0;                      /* No error */
		d = 0;                      /* Crc at end of calculation is O */
		i = n + m + 2;              /* Places pointer on last element */
		do
		{
			c = CrcRev(c, s[i & l]);            /* Calculate previous crc */
			if (CrcChar(c, d, &t) == 0)         /* Calculate char */
			{
				if (t != s[i & l])                /* New char placed in string  */
				{
					s[i & l] = t;
					e = 1;                          /* 1 error corrected */
				}
			}
			d = CrcRev(d, s[i & l]);            /* Calculate previous crc */
			i --;
		}
		while ((n <= i) && (e == 0));
	}
	else
	{
		e = 0;
	}
	return e;
}

ulong Sqrt(ulong x)
{
	/* Calculate square root of n */
	/* Heron algorythm */
	ulong y;
	ulong z;
	if (x == 0)
		y = 0;
	else
	{
		y = 1;
		do
		{
			z = y;
			y = (y + (x / y)) / 2;
		}
		while (1 < (z - y));
	}
	return y;
}

